# 심사위원의 투표 결과가 주어졌을 때, 어떤 사람이 우승하는지 구하는 프로그램을 작성하시오.

import string


count = int(input())
who = list(str(input()))
a = 0
b = 0

for w in who:
    if w == 'A':
        a += 1
    else:
        b += 1

if a > b:
    print('A')
elif a < b:
    print('B')
else:
    print("Tie")